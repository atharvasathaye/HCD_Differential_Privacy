        print(f"Privacy-preserving data points: {len(private_lat_valid)}")
        print(f"Noise statistics - Lat: mean={np.mean(lat_noise):.6f}, std={np.std(lat_noise):.6f}, max={np.max(np.abs(lat_noise)):.6f}")
