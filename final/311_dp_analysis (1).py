"""## 4. Differential Privacy Mechanism Implementation"""

class DifferentialPrivacyAnalyzer:
    """
    Differential Privacy Analyzer
    Implements Laplace mechanism and privacy budget management
    """

    def __init__(self, data, epsilon=1.0):
        self.data = data
        self.epsilon = epsilon
        self.privacy_budget_used = 0.0
        self.results = {}
        self.time_noise_offset = None  # Store time noise offset

    def add_laplace_noise(self, true_value, sensitivity=1.0, epsilon_used=None):
        """
        Add Laplace noise

        Args:
            true_value: True value
            sensitivity: Sensitivity
            epsilon_used: Privacy budget used
        """
        if epsilon_used is None:
            epsilon_used = 0.1

        scale = sensitivity / epsilon_used
        noise = np.random.laplace(0, scale)
        return true_value + noise

    def private_count_query(self, column, epsilon_portion=0.3):
        """Privacy-preserving count query"""
        if self.privacy_budget_used + epsilon_portion > self.epsilon:
            print(f" Privacy budget insufficient: Used {self.privacy_budget_used:.2f}, Required {epsilon_portion:.2f}")
            return None

        print(f" Executing privacy-preserving count query: {column}")

        # Get non-null values
        valid_data = self.data[column].dropna()

        if len(valid_data) == 0:
            print(f" Column {column} has no valid data")
            return None

        # Calculate true count
        actual_count = len(valid_data)

        # Add Laplace noise
        private_count = self.add_laplace_noise(actual_count, sensitivity=1.0, epsilon_used=epsilon_portion)
        private_count = max(0, int(private_count))  # Ensure non-negative integer

        self.privacy_budget_used += epsilon_portion

        result = {
            'column': column,
            'private_count': private_count,
            'actual_count': actual_count,
            'epsilon_used': epsilon_portion,
            'noise_level': abs(actual_count - private_count) / actual_count * 100 if actual_count > 0 else 0
        }

        print(f" Query completed: {column}")
        print(f" Actual count: {actual_count:,}")
        print(f" Privacy-preserving count: {private_count:,}")
        print(f" Noise level: {result['noise_level']:.2f}%")
        print(f" Privacy budget used: {self.privacy_budget_used:.2f}/{self.epsilon}")

        return result

    def private_categorical_analysis(self, column, epsilon_portion=0.4):
        """Privacy-preserving categorical data analysis"""
        if self.privacy_budget_used + epsilon_portion > self.epsilon:
            print(f" Privacy budget insufficient")
            return None

        print(f" Executing privacy-preserving categorical analysis: {column}")

        # Get non-null values
        valid_data = self.data[column].dropna()

        if len(valid_data) == 0:
            print(f" Column {column} has no valid data")
            return None

        # Get category distribution
        value_counts = valid_data.value_counts()

        # Add noise for each category
        private_counts = {}
        epsilon_per_category = epsilon_portion / len(value_counts)

        for category, count in value_counts.items():
            # Add Laplace noise
            private_count = self.add_laplace_noise(count, sensitivity=1.0, epsilon_used=epsilon_per_category)
            private_count = max(0, int(private_count))  # Ensure non-negative
            private_counts[category] = private_count

        self.privacy_budget_used += epsilon_portion

        result = {
            'column': column,
            'private_counts': private_counts,
            'actual_counts': value_counts.to_dict(),
            'epsilon_used': epsilon_portion,
            'total_noise_level': abs(sum(value_counts.values) - sum(private_counts.values())) / sum(value_counts.values) * 100
        }

        print(f" Categorical analysis completed: {column}")
        print(f" Total noise level: {result['total_noise_level']:.2f}%")
        print(f" Privacy budget used: {self.privacy_budget_used:.2f}/{self.epsilon}")

        return result

    def private_numerical_analysis(self, column, epsilon_portion=0.2):
        """Privacy-preserving numerical analysis"""
        if self.privacy_budget_used + epsilon_portion > self.epsilon:
            print(f" Privacy budget insufficient")
            return None

        print(f" Executing privacy-preserving numerical analysis: {column}")

        # Get non-null numerical values
        valid_data = self.data[column].dropna()

        if len(valid_data) == 0:
            print(f" Column {column} has no valid data")
            return None

        # Calculate basic statistics
        actual_mean = valid_data.mean()
        actual_std = valid_data.std()
        actual_count = len(valid_data)

        # Add Laplace noise
        private_mean = self.add_laplace_noise(actual_mean, sensitivity=1.0, epsilon_used=epsilon_portion)
        private_std = self.add_laplace_noise(actual_std, sensitivity=1.0, epsilon_used=epsilon_portion)

        self.privacy_budget_used += epsilon_portion

        result = {
            'column': column,
            'private_mean': private_mean,
            'actual_mean': actual_mean,
            'private_std': private_std,
            'actual_std': actual_std,
            'count': actual_count,
            'epsilon_used': epsilon_portion,
            'mean_error': abs(actual_mean - private_mean) / abs(actual_mean) * 100 if actual_mean != 0 else 0
        }

        print(f" Numerical analysis completed: {column}")
        print(f" Actual mean: {actual_mean:.4f}, Privacy-preserving mean: {private_mean:.4f}")
        print(f" Actual std: {actual_std:.4f}, Privacy-preserving std: {private_std:.4f}")
        print(f" Mean relative error: {result['mean_error']:.2f}%")
        print(f" Privacy budget used: {self.privacy_budget_used:.2f}/{self.epsilon}")

        return result

    def private_geographic_analysis(self, epsilon_portion=0.1):
        """Privacy-preserving geographic analysis"""
        if self.privacy_budget_used + epsilon_portion > self.epsilon:
            print(f" Privacy budget insufficient")
            return None

        print(f" Executing privacy-preserving geographic analysis")

        # Analyze records with coordinate data
        coord_data = self.data.dropna(subset=['LATITUDE', 'LONGITUDE'])

        if len(coord_data) == 0:
            print(" No valid coordinate data")
            return None

        # Geographic region aggregation (based on first 3 digits of ZIP_CODE)
        private_area_counts = {}
        actual_area_counts = {}

        if 'ZIP_CODE' in coord_data.columns:
            area_counts = coord_data['ZIP_CODE'].astype(str).str[:3].value_counts()
            actual_area_counts = area_counts.to_dict()

            # Add noise
            epsilon_per_area = epsilon_portion / len(area_counts)

            for area, count in area_counts.items():
                private_count = self.add_laplace_noise(count, sensitivity=1.0, epsilon_used=epsilon_per_area)
                private_count = max(0, int(private_count))
                private_area_counts[area] = private_count

        self.privacy_budget_used += epsilon_portion

        result = {
            'total_records_with_coords': len(coord_data),
            'private_area_counts': private_area_counts,
            'actual_area_counts': actual_area_counts,
            'epsilon_used': epsilon_portion
        }

        print(f" Geographic analysis completed")
        print(f" Privacy budget used: {self.privacy_budget_used:.2f}/{self.epsilon}")

        return result

    def private_time_analysis(self, time_column='CREATED_DATE', epsilon_portion=0.15):
        """
        Privacy-preserving time analysis
        Add the same noise offset to all timestamps to keep the original time distribution unchanged

        Args:
            time_column: Time column name
            epsilon_portion: Privacy budget portion used
        """
        if self.privacy_budget_used + epsilon_portion > self.epsilon:
            print(f" Privacy budget insufficient")
            return None

        print(f"⏰ Executing privacy-preserving time analysis: {time_column}")

        # Get valid time data
        time_data = self.data[time_column].dropna()

        if len(time_data) == 0:
            print(f" Column {time_column} has no valid data")
            return None

        # Ensure datetime type
        if not pd.api.types.is_datetime64_any_dtype(time_data):
            time_data = pd.to_datetime(time_data, errors='coerce')
            time_data = time_data.dropna()

        if len(time_data) == 0:
            print(f" Unable to parse time data")
            return None

        # Calculate time range (in days)
        time_min = time_data.min()
        time_max = time_data.max()
        time_range_days = (time_max - time_min).days

        # Calculate sensitivity (in days, maximum possible time difference)
        # For timestamps, sensitivity is the time range
        sensitivity_days = time_range_days if time_range_days > 0 else 1

        # Generate single noise offset (use same offset for all records)
        # Use Laplace noise, but generate only once
        scale = sensitivity_days / epsilon_portion
        noise_days = np.random.laplace(0, scale)

        # Store noise offset
        self.time_noise_offset = pd.Timedelta(days=noise_days)

        # Create privacy-preserving time data (add same offset to all records)
        private_time_data = time_data + self.time_noise_offset

        # Calculate statistics
        # Aggregate by date
        actual_daily = time_data.dt.date.value_counts().sort_index()
        private_daily = private_time_data.dt.date.value_counts().sort_index()

        # Aggregate by hour
        actual_hourly = time_data.dt.hour.value_counts().sort_index()
        private_hourly = private_time_data.dt.hour.value_counts().sort_index()

        # Aggregate by day of week
        actual_weekly = time_data.dt.day_name().value_counts()
        private_weekly = private_time_data.dt.day_name().value_counts()

        # Calculate distribution similarity
        # Since using the same offset, hour and day-of-week distributions should be completely consistent
        # Calculate hour distribution overlap (should be close to 100%)
        all_hours = set(actual_hourly.index) | set(private_hourly.index)
        if len(all_hours) > 0:
            # Calculate match for each hour
            hour_matches = 0
            for h in all_hours:
                actual_count = actual_hourly.get(h, 0)
                private_count = private_hourly.get(h, 0)
                # Since using same offset, hours should match completely
                if actual_count == private_count:
                    hour_matches += actual_count
            hour_overlap = (hour_matches / len(time_data) * 100) if len(time_data) > 0 else 100
        else:
            hour_overlap = 100  # If no data, set to 100%

        self.privacy_budget_used += epsilon_portion

        result = {
            'column': time_column,
            'noise_offset_days': noise_days,
            'noise_offset': str(self.time_noise_offset),
            'actual_daily_counts': actual_daily.to_dict(),
            'private_daily_counts': private_daily.to_dict(),
            'actual_hourly_counts': actual_hourly.to_dict(),
            'private_hourly_counts': private_hourly.to_dict(),
            'actual_weekly_counts': actual_weekly.to_dict(),
            'private_weekly_counts': private_weekly.to_dict(),
            'hour_distribution_overlap': hour_overlap,
            'epsilon_used': epsilon_portion,
            'total_records': len(time_data)
        }

        print(f" Time analysis completed: {time_column}")
        print(f" Time noise offset: {noise_days:.2f} days ({self.time_noise_offset})")
        print(f" Hour distribution overlap: {hour_overlap:.2f}%")
        print(f" Privacy budget used: {self.privacy_budget_used:.2f}/{self.epsilon}")

        return result

    def apply_time_noise_to_data(self, time_column='CREATED_DATE'):
        """
        Apply time noise to dataset (create new privacy-preserving time column)

        Args:
            time_column: Time column name
        """
        if self.time_noise_offset is None:
            print(" Please run time analysis first to generate noise offset")
            return None

        if time_column not in self.data.columns:
            print(f" Column {time_column} does not exist")
            return None

        # Create privacy-preserving time column
        private_time_col = f'{time_column}_PRIVATE'
        self.data[private_time_col] = pd.to_datetime(self.data[time_column], errors='coerce')

        # Apply same noise offset
        mask = self.data[private_time_col].notna()
        self.data.loc[mask, private_time_col] = self.data.loc[mask, private_time_col] + self.time_noise_offset

        print(f" Time noise applied to column: {private_time_col}")
        return private_time_col

    def generate_privacy_report(self):
        """
        Generate privacy protection report
        """
        print("\n" + "="*60)
        print(" Differential Privacy Analysis Report")
        print("="*60)
        print(f" Dataset size: {len(self.data):,} records")
        print(f" Privacy budget (ε): {self.epsilon}")
        print(f" Budget used: {self.privacy_budget_used:.3f}")
        print(f" Remaining budget: {self.epsilon - self.privacy_budget_used:.3f}")
        print(f" Budget utilization: {(self.privacy_budget_used/self.epsilon)*100:.1f}%")
        print("="*60)

        if self.privacy_budget_used > self.epsilon:
            print(" Warning: Privacy budget exceeded!")
        else:
            print(" Privacy budget usage normal")

    def run_complete_analysis(self):
        """
        Run complete differential privacy analysis
        """
        print(" Starting differential privacy analysis...")

        # 1. Basic statistical queries
        print("Executing basic statistical queries...")
        basic_stats = self.private_count_query('SR_TYPE', 0.15)

        # 2. Categorical analysis
        print("Executing categorical analysis...")
        sr_type_analysis = self.private_categorical_analysis('SR_TYPE', 0.25)
        status_analysis = self.private_categorical_analysis('STATUS', 0.15)

        # 3. Numerical analysis
        print("Executing numerical analysis...")
        lat_analysis = None
        lon_analysis = None
        if 'LATITUDE' in self.data.columns:
            lat_analysis = self.private_numerical_analysis('LATITUDE', 0.1)
        if 'LONGITUDE' in self.data.columns:
            lon_analysis = self.private_numerical_analysis('LONGITUDE', 0.1)

        # 4. Geographic analysis
        print("Executing geographic analysis...")
        geo_analysis = self.private_geographic_analysis(0.1)

        # 5. Time analysis
        print("Executing time analysis...")
        time_analysis = None
        if 'CREATED_DATE' in self.data.columns:
            time_analysis = self.private_time_analysis('CREATED_DATE', 0.15)
            # Apply time noise to data
            if time_analysis:
                self.apply_time_noise_to_data('CREATED_DATE')

        # 6. Generate report
        self.generate_privacy_report()

        # Save results
        self.results = {
            'basic_stats': basic_stats,
            'sr_type_analysis': sr_type_analysis,
            'status_analysis': status_analysis,
            'lat_analysis': lat_analysis,
            'lon_analysis': lon_analysis,
            'geo_analysis': geo_analysis,
            'time_analysis': time_analysis
        }

        return self.results

