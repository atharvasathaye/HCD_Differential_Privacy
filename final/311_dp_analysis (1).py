        # Improved noise generation to prevent clustering
        def improved_geographic_noise(coord_values, scale_factor=0.003, truncation_factor=2.0):
            """
            Generate improved geographic noise that prevents clustering
            Uses truncated Laplace with uniform component for better distribution
            """
            coord_range = np.max(coord_values) - np.min(coord_values)
            noise_scale = coord_range * scale_factor

            # Generate noise with mix of truncated Laplace and uniform component
            # This prevents extreme clustering while maintaining privacy
            laplace_noise = np.random.laplace(0, noise_scale, len(coord_values))
            max_noise = truncation_factor * noise_scale
            laplace_noise = np.clip(laplace_noise, -max_noise, max_noise)

            # Add small uniform component to break up clusters
            uniform_component = np.random.uniform(-noise_scale * 0.3, noise_scale * 0.3, len(coord_values))

            # Combine: 80% truncated Laplace + 20% uniform
            combined_noise = 0.8 * laplace_noise + 0.2 * uniform_component

