def remove_coordinate_outliers(data, lat_col='LATITUDE', lon_col='LONGITUDE', method='iqr'):
    """
    Remove outliers from coordinate data

    Args:
        data: DataFrame
        lat_col: Latitude column name
        lon_col: Longitude column name
        method: Outlier detection method ('iqr' or 'zscore')

    Returns:
        Processed DataFrame and number of removed outliers
    """
    original_count = len(data)

    # Get records with coordinate data
    coord_data = data.dropna(subset=[lat_col, lon_col]).copy()

    if len(coord_data) == 0:
        print(" No coordinate data to process")
        return data, 0

    if method == 'iqr':
        # Use IQR method to detect outliers
        for col in [lat_col, lon_col]:
            Q1 = coord_data[col].quantile(0.25)
            Q3 = coord_data[col].quantile(0.75)
            IQR = Q3 - Q1
            lower_bound = Q1 - 1.5 * IQR
            upper_bound = Q3 + 1.5 * IQR

            # Mark outliers
            outliers = (coord_data[col] < lower_bound) | (coord_data[col] > upper_bound)
            coord_data.loc[outliers, col] = np.nan
            print(f"  {col}: Removed {outliers.sum()} outliers (range: [{lower_bound:.4f}, {upper_bound:.4f}])")

    elif method == 'zscore':
        # Use Z-score method to detect outliers
        from scipy import stats
        for col in [lat_col, lon_col]:
            z_scores = np.abs(stats.zscore(coord_data[col].dropna()))
            threshold = 3
            outliers = z_scores > threshold
            coord_data.loc[coord_data[col].notna() & outliers, col] = np.nan
            print(f"  {col}: Removed {outliers.sum()} outliers (Z-score > {threshold})")

    # Remove records with missing coordinates
    coord_data = coord_data.dropna(subset=[lat_col, lon_col])

    # Update original DataFrame
    data.loc[coord_data.index, [lat_col, lon_col]] = coord_data[[lat_col, lon_col]]

    # Remove records with completely missing coordinates
    data = data.dropna(subset=[lat_col, lon_col], how='all')

    removed_count = original_count - len(data)
    print(f" Coordinate outlier processing completed: Removed {removed_count} records")

    return data, removed_count

