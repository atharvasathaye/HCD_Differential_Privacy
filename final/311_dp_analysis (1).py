# Custom JSON encoder to handle date objects and other non-serializable types
class CustomJSONEncoder(json.JSONEncoder):
    def default(self, obj):
        if isinstance(obj, (date, datetime)):
            return obj.isoformat()
        elif isinstance(obj, (np.integer, np.floating)):
            return float(obj)
        elif isinstance(obj, np.ndarray):
            return obj.tolist()
        elif hasattr(obj, '__dict__'):
            return str(obj)
        return super().default(obj)

def convert_dict_keys_to_str(obj):
    """Recursively convert dictionary keys to strings"""
    if isinstance(obj, dict):
        new_dict = {}
        for key, value in obj.items():
            # Convert key to string if it's not already
            str_key = str(key) if not isinstance(key, str) else key
            # Recursively process value
            new_dict[str_key] = convert_dict_keys_to_str(value)
        return new_dict
    elif isinstance(obj, list):
        return [convert_dict_keys_to_str(item) for item in obj]
    elif isinstance(obj, (date, datetime)):
        return obj.isoformat()
    elif isinstance(obj, (np.integer, np.floating)):
        return float(obj)
    elif isinstance(obj, np.ndarray):
        return obj.tolist()
    else:
        return obj

