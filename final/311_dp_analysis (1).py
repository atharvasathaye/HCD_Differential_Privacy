# Coordinate error analysis function
def analyze_coordinate_errors(data, results):
    """
    Analyze errors between actual coordinates and privacy-preserving coordinates
    """
    print("="*80)
    print("Coordinate Error Analysis")
    print("="*80)

    # Check if coordinate data exists
    coord_data = data.dropna(subset=['LATITUDE', 'LONGITUDE'])

    if len(coord_data) == 0:
        print("No coordinate data available for error analysis")
        return None

    print(f"\nTotal records with coordinates: {len(coord_data):,}")

    # Get actual coordinates
    actual_lat = coord_data['LATITUDE'].values
    actual_lon = coord_data['LONGITUDE'].values

    # Calculate privacy-preserving coordinates based on analysis results
    private_lat = actual_lat.copy()
    private_lon = actual_lon.copy()

    if results.get('lat_analysis') and results.get('lon_analysis'):
        lat_data = results['lat_analysis']
        lon_data = results['lon_analysis']

        # Get noise parameters
        actual_lat_mean = lat_data['actual_mean']
        private_lat_mean = lat_data['private_mean']
        actual_lon_mean = lon_data['actual_mean']
        private_lon_mean = lon_data['private_mean']

        # Calculate mean shift
        lat_mean_shift = private_lat_mean - actual_lat_mean
        lon_mean_shift = private_lon_mean - actual_lon_mean

        # Apply mean shift to all coordinates (simplified approach)
        # In practice, each coordinate would have individual noise
        private_lat = actual_lat + lat_mean_shift
        private_lon = actual_lon + lon_mean_shift

        print(f"\nCoordinate Statistics from Analysis:")
        print(f"  Latitude - Actual mean: {actual_lat_mean:.6f}, Private mean: {private_lat_mean:.6f}")
        print(f"  Longitude - Actual mean: {actual_lon_mean:.6f}, Private mean: {private_lon_mean:.6f}")
        print(f"  Mean shift - Lat: {lat_mean_shift:.6f}, Lon: {lon_mean_shift:.6f}")
    else:
        print("\nWarning: No coordinate analysis results found. Using actual coordinates as baseline.")
        print("Please run coordinate analysis first.")
        return None

    # Calculate errors
    lat_errors = np.abs(actual_lat - private_lat)
    lon_errors = np.abs(actual_lon - private_lon)

    # Convert coordinate errors to meters
    # 1 degree latitude ≈ 111,000 meters
    # 1 degree longitude ≈ 111,000 * cos(latitude) meters
    lat_errors_meters = lat_errors * 111000
    lon_errors_meters = lon_errors * 111000 * np.cos(np.radians(actual_lat))

    # Calculate Euclidean distance error in meters
    distance_errors_meters = np.sqrt(lat_errors_meters**2 + lon_errors_meters**2)

    # Calculate relative errors (as percentage of coordinate range)
    lat_range = np.max(actual_lat) - np.min(actual_lat)
    lon_range = np.max(actual_lon) - np.min(actual_lon)
    lat_relative_errors = (lat_errors / lat_range * 100) if lat_range > 0 else np.zeros_like(lat_errors)
    lon_relative_errors = (lon_errors / lon_range * 100) if lon_range > 0 else np.zeros_like(lon_errors)

    # Create error DataFrame
    error_df = pd.DataFrame({
        'Actual_Lat': actual_lat,
        'Actual_Lon': actual_lon,
        'Private_Lat': private_lat,
        'Private_Lon': private_lon,
        'Lat_Error_Degrees': lat_errors,
        'Lon_Error_Degrees': lon_errors,
        'Lat_Error_Meters': lat_errors_meters,
        'Lon_Error_Meters': lon_errors_meters,
        'Distance_Error_Meters': distance_errors_meters,
        'Lat_Relative_Error_%': lat_relative_errors,
        'Lon_Relative_Error_%': lon_relative_errors
    })

    # Summary statistics
    print("\n" + "="*80)
    print("Error Summary Statistics")
    print("="*80)

    print(f"\nLatitude Errors:")
    print(f"  Mean absolute error: {lat_errors.mean():.6f} degrees ({lat_errors_meters.mean():.1f} meters)")
    print(f"  Median absolute error: {np.median(lat_errors):.6f} degrees ({np.median(lat_errors_meters):.1f} meters)")
    print(f"  Max absolute error: {lat_errors.max():.6f} degrees ({lat_errors_meters.max():.1f} meters)")
    print(f"  Std deviation: {lat_errors.std():.6f} degrees ({lat_errors_meters.std():.1f} meters)")
    print(f"  Mean relative error: {lat_relative_errors.mean():.4f}%")

    print(f"\nLongitude Errors:")
    print(f"  Mean absolute error: {lon_errors.mean():.6f} degrees ({lon_errors_meters.mean():.1f} meters)")
    print(f"  Median absolute error: {np.median(lon_errors):.6f} degrees ({np.median(lon_errors_meters):.1f} meters)")
    print(f"  Max absolute error: {lon_errors.max():.6f} degrees ({lon_errors_meters.max():.1f} meters)")
    print(f"  Std deviation: {lon_errors.std():.6f} degrees ({lon_errors_meters.std():.1f} meters)")
    print(f"  Mean relative error: {lon_relative_errors.mean():.4f}%")

    print(f"\nDistance Errors (Euclidean):")
    print(f"  Mean distance error: {distance_errors_meters.mean():.1f} meters")
    print(f"  Median distance error: {np.median(distance_errors_meters):.1f} meters")
    print(f"  Max distance error: {distance_errors_meters.max():.1f} meters")
    print(f"  Std deviation: {distance_errors_meters.std():.1f} meters")
    print(f"  95th percentile: {np.percentile(distance_errors_meters, 95):.1f} meters")
    print(f"  99th percentile: {np.percentile(distance_errors_meters, 99):.1f} meters")

    # Error metrics
    mse_lat = np.mean(lat_errors**2)
    mse_lon = np.mean(lon_errors**2)
    rmse_lat = np.sqrt(mse_lat)
    rmse_lon = np.sqrt(mse_lon)
    mse_distance = np.mean(distance_errors_meters**2)
    rmse_distance = np.sqrt(mse_distance)

    print(f"\nError Metrics:")
    print(f"  Latitude MSE: {mse_lat:.8f} (degrees²), RMSE: {rmse_lat:.6f} degrees")
    print(f"  Longitude MSE: {mse_lon:.8f} (degrees²), RMSE: {rmse_lon:.6f} degrees")
